  function cloneSelected(selected) {
    var out = {};
    var key;

    if (!selected) {
      return out;
    }

    for (key in selected) {
      if (Object.prototype.hasOwnProperty.call(selected, key)) {
        out[key] = selected[key];
      }
    }

    return out;
  }

  function getSelected(shopIndex, visitorIndex) {
    var shop = PRESETS[shopIndex];
    var preset;

    if (!shop || visitorIndex == null || visitorIndex < 0 || visitorIndex >= shop.length) {
      return {};
    }

    preset = shop[visitorIndex];
    return preset ? cloneSelected(preset) : {};
  }

  function resolveForVisitor(shopIndex, visitor, visitorIndex) {
    var inline = visitor && visitor.dress && visitor.dress.selected;
    var selected = inline ? cloneSelected(inline) : getSelected(shopIndex, visitorIndex);
    var hasItem = false;
    var key;

    for (key in selected) {
      if (Object.prototype.hasOwnProperty.call(selected, key) && selected[key]) {
        hasItem = true;
        break;
      }
    }

    return hasItem ? selected : getSelected(shopIndex, visitorIndex);
  }

