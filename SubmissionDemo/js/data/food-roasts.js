  function normalizeName(name) {
    return String(name || "").trim();
  }

  function get(foodName) {
    var name = normalizeName(foodName);

    if (!name) {
      return { foodName: "", text: FALLBACK_ROAST };
    }

    return {
      foodName: name,
      text: FOOD_ROASTS[name] || ("今天穿成了" + name + "，但我的自信还在备餐中。")
    };
  }

  function pick(candidates) {
    var list = [];
    var seen = {};

    (candidates || []).forEach(function (name) {
      var normalized = normalizeName(name);
      if (!normalized || seen[normalized]) {
        return;
      }
      seen[normalized] = true;
      list.push(normalized);
    });

    if (!list.length) {
      return get("");
    }

    return get(list[Math.floor(Math.random() * list.length)]);
  }

  function has(foodName) {
    return Object.prototype.hasOwnProperty.call(FOOD_ROASTS, normalizeName(foodName));
  }

  global.Yummi = global.Yummi || {};
